import { afterEach, describe, expect, it } from 'vitest'

import { enumLabel, formatList, resolveCopy, translate } from '@/i18n/copy'
import i18n from '@/plugins/i18n'

const originalLocale = i18n.global.locale.value

describe('desktop i18n smoke coverage', () => {
  afterEach(() => {
    i18n.global.locale.value = originalLocale
  })

  it('switches shell labels and enum labels between zh-CN and en-US', () => {
    i18n.global.locale.value = 'zh-CN'

    expect(translate('dashboard.header.eyebrow')).toBe('工作区 Dashboard')
    expect(translate('conversation.stream.title')).toBe('消息流')
    expect(translate('settings.tabs.general')).toBe('通用设置')
    expect(resolveCopy('sidebar.navigation.overview')).toBe('概览')
    expect(resolveCopy('connections.header.title')).toBe('工作区连接')
    expect(enumLabel('conversationIntent', 'paused')).toBe('已暂停')
    expect(enumLabel('recordStatus', 'active')).toBe('正常')
    expect(enumLabel('passwordState', 'set')).toBe('已设置')
    expect(enumLabel('rbacPermissionKind', 'atomic')).toBe('原子权限')
    expect(enumLabel('rbacPermissionTargetType', 'user')).toBe('用户')
    expect(enumLabel('menuSource', 'main-sidebar')).toBe('主侧边栏')
    expect(enumLabel('menuSource', 'console')).toBe('控制台')
    expect(enumLabel('menuSource', 'access-control')).toBe('访问控制')
    expect(translate('sidebar.navigation.console')).toBe('控制台')
    expect(translate('sidebar.navigation.accessControl')).toBe('访问控制')
    expect(translate('sidebar.navigation.personalCenter')).toBe('个人中心')
    expect(translate('accessControl.header.eyebrow')).toBe('访问控制')
    expect(translate('accessControl.header.title')).toBe('访问控制')
    expect(translate('accessControl.nav.users')).toBe('用户管理')
    expect(translate('accessControl.nav.org')).toBe('组织管理')
    expect(translate('accessControl.nav.roles')).toBe('角色管理')
    expect(translate('accessControl.nav.policies')).toBe('权限与策略')
    expect(translate('accessControl.nav.menus')).toBe('菜单管理')
    expect(translate('accessControl.nav.resources')).toBe('资源与授权')
    expect(translate('accessControl.nav.sessions')).toBe('会话与审计')
    expect(translate('personalCenter.profile.edit.title')).toBe('编辑个人资料')
    expect(translate('personalCenter.profile.access.title')).toBe('访问概览')
    expect(translate('personalCenter.profile.access.openRoles')).toBe('查看角色')
    expect(translate('personalCenter.profile.edit.fields.displayName')).toBe('昵称')
    expect(translate('personalCenter.profile.edit.fields.avatar')).toBe('头像')
    expect(translate('personalCenter.profile.edit.hints.avatarCurrent')).toBe('当前头像')
    expect(translate('personalCenter.profile.edit.actions.pickAvatar')).toBe('选择头像')
    expect(translate('personalCenter.profile.password.title')).toBe('修改密码')
    expect(translate('personalCenter.profile.password.fields.currentPassword')).toBe('当前密码')
    expect(translate('personalCenter.profile.feedback.profileSaved')).toBe('个人资料已保存。')
    expect(translate('personalCenter.profile.feedback.passwordUpdated')).toBe('密码已更新。')
    expect(translate('personalCenter.profile.validation.passwordMismatch')).toBe('两次输入的新密码不一致。')
    expect(enumLabel('rbacPermissionTargetType', 'resource')).toBe('资源')
    expect(enumLabel('rbacPermissionTargetType', 'knowledge')).toBe('知识')
    expect(translate('console.header.title')).toBe('控制台')
    expect(translate('tools.actions.copyToManaged')).toBe('复制到托管资源')
    expect(translate('tools.detail.consumers')).toBe('使用者')
    expect(translate('tools.ownerScopes.builtin')).toBe('内置')
    expect(translate('tools.ownerScopes.workspace')).toBe('工作区')
    expect(translate('tools.ownerScopes.project')).toBe('项目')

    i18n.global.locale.value = 'en-US'

    expect(translate('dashboard.header.eyebrow')).toBe('Workspace Dashboard')
    expect(translate('conversation.stream.title')).toBe('Message Stream')
    expect(translate('settings.tabs.general')).toBe('General Settings')
    expect(resolveCopy('sidebar.navigation.overview')).toBe('Overview')
    expect(resolveCopy('connections.header.title')).toBe('Workspace Connections')
    expect(enumLabel('conversationIntent', 'paused')).toBe('Paused')
    expect(enumLabel('recordStatus', 'active')).toBe('Active')
    expect(enumLabel('passwordState', 'set')).toBe('Set')
    expect(enumLabel('rbacPermissionKind', 'atomic')).toBe('Atomic')
    expect(enumLabel('rbacPermissionTargetType', 'user')).toBe('User')
    expect(enumLabel('menuSource', 'main-sidebar')).toBe('Main Sidebar')
    expect(enumLabel('menuSource', 'console')).toBe('Console')
    expect(enumLabel('menuSource', 'access-control')).toBe('Access Control')
    expect(translate('sidebar.navigation.console')).toBe('Console')
    expect(translate('sidebar.navigation.accessControl')).toBe('Access Control')
    expect(translate('sidebar.navigation.personalCenter')).toBe('Personal Center')
    expect(translate('accessControl.header.eyebrow')).toBe('Access Control')
    expect(translate('accessControl.header.title')).toBe('Access Control')
    expect(translate('accessControl.nav.users')).toBe('Users')
    expect(translate('accessControl.nav.org')).toBe('Organization')
    expect(translate('accessControl.nav.roles')).toBe('Roles')
    expect(translate('accessControl.nav.policies')).toBe('Permissions & Policies')
    expect(translate('accessControl.nav.menus')).toBe('Menus')
    expect(translate('accessControl.nav.resources')).toBe('Resources & Authorization')
    expect(translate('accessControl.nav.sessions')).toBe('Sessions & Audit')
    expect(translate('personalCenter.profile.edit.title')).toBe('Edit Profile')
    expect(translate('personalCenter.profile.access.title')).toBe('Access Overview')
    expect(translate('personalCenter.profile.access.openRoles')).toBe('View roles')
    expect(translate('personalCenter.profile.edit.fields.displayName')).toBe('Display name')
    expect(translate('personalCenter.profile.edit.fields.avatar')).toBe('Avatar')
    expect(translate('personalCenter.profile.edit.hints.avatarCurrent')).toBe('Current avatar')
    expect(translate('personalCenter.profile.edit.actions.pickAvatar')).toBe('Choose avatar')
    expect(translate('personalCenter.profile.password.title')).toBe('Change Password')
    expect(translate('personalCenter.profile.password.fields.currentPassword')).toBe('Current password')
    expect(translate('personalCenter.profile.feedback.profileSaved')).toBe('Profile saved.')
    expect(translate('personalCenter.profile.feedback.passwordUpdated')).toBe('Password updated.')
    expect(translate('personalCenter.profile.validation.passwordMismatch')).toBe('The new passwords do not match.')
    expect(enumLabel('rbacPermissionTargetType', 'resource')).toBe('Resource')
    expect(enumLabel('rbacPermissionTargetType', 'knowledge')).toBe('Knowledge')
    expect(translate('console.header.title')).toBe('Console')
    expect(translate('tools.actions.copyToManaged')).toBe('Copy to Managed')
    expect(translate('tools.detail.consumers')).toBe('Consumers')
    expect(translate('tools.ownerScopes.builtin')).toBe('Built-in')
    expect(translate('tools.ownerScopes.workspace')).toBe('Workspace')
    expect(translate('tools.ownerScopes.project')).toBe('Project')
    expect(translate('accessControl.common.capabilityModules.workspace')).toBe('Workspace')
    expect(translate('accessControl.common.capabilityModules.providerCredential')).toBe('Provider Credentials')
  })

  it('formats localized list separators for access-control summaries', () => {
    i18n.global.locale.value = 'zh-CN'
    expect(formatList(['项目一', '项目二', '项目三'])).toBe('项目一、项目二、项目三')

    i18n.global.locale.value = 'en-US'
    expect(formatList(['Project One', 'Project Two', 'Project Three'])).toBe('Project One, Project Two, and Project Three')
  })
})
