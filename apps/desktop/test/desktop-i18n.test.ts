import { afterEach, describe, expect, it } from 'vitest'

import { enumLabel, resolveCopy, translate } from '@/i18n/copy'
import i18n from '@/plugins/i18n'

const originalLocale = i18n.global.locale.value

describe('desktop i18n smoke coverage', () => {
  afterEach(() => {
    i18n.global.locale.value = originalLocale
  })

  it('switches shell labels and enum labels between zh-CN and en-US', () => {
    i18n.global.locale.value = 'zh-CN'

    expect(translate('dashboard.header.eyebrow')).toBe('工作区 Dashboard')
    expect(translate('conversation.stream.title')).toBe('消息流')
    expect(translate('settings.tabs.general')).toBe('通用设置')
    expect(resolveCopy('sidebar.navigation.overview')).toBe('概览')
    expect(resolveCopy('connections.header.title')).toBe('工作区连接')
    expect(enumLabel('conversationIntent', 'paused')).toBe('已暂停')
    expect(enumLabel('recordStatus', 'active')).toBe('正常')
    expect(enumLabel('passwordState', 'set')).toBe('已设置')
    expect(enumLabel('rbacPermissionKind', 'atomic')).toBe('原子权限')
    expect(enumLabel('rbacPermissionTargetType', 'user')).toBe('用户')
    expect(enumLabel('menuSource', 'main-sidebar')).toBe('主侧边栏')
    expect(translate('userCenter.profile.edit.title')).toBe('编辑个人资料')
    expect(translate('userCenter.profile.access.title')).toBe('访问概览')
    expect(translate('userCenter.profile.access.openRoles')).toBe('查看角色')
    expect(translate('userCenter.profile.edit.fields.displayName')).toBe('昵称')
    expect(translate('userCenter.profile.edit.fields.avatar')).toBe('头像')
    expect(translate('userCenter.profile.edit.hints.avatarCurrent')).toBe('当前头像')
    expect(translate('userCenter.profile.edit.actions.pickAvatar')).toBe('选择头像')
    expect(translate('userCenter.profile.password.title')).toBe('修改密码')
    expect(translate('userCenter.profile.password.fields.currentPassword')).toBe('当前密码')
    expect(translate('userCenter.profile.feedback.profileSaved')).toBe('个人资料已保存。')
    expect(translate('userCenter.profile.feedback.passwordUpdated')).toBe('密码已更新。')
    expect(translate('userCenter.profile.validation.passwordMismatch')).toBe('两次输入的新密码不一致。')
    expect(translate('userCenter.users.actions.create')).toBe('新建成员')
    expect(translate('userCenter.users.actions.delete')).toBe('删除成员')
    expect(translate('userCenter.users.fields.role')).toBe('角色')
    expect(translate('userCenter.users.fields.scopeProjects')).toBe('项目范围')
    expect(translate('userCenter.users.avatar.actions.useDefault')).toBe('使用默认头像')
    expect(translate('userCenter.users.password.actions.useDefault')).toBe('使用默认密码')
    expect(translate('userCenter.roles.actions.create')).toBe('新建角色')
    expect(translate('userCenter.roles.actions.delete')).toBe('删除角色')
    expect(translate('userCenter.roles.fields.permissions')).toBe('权限')
    expect(translate('userCenter.roles.fields.menus')).toBe('菜单')
    expect(translate('userCenter.roles.menuGroups.app')).toBe('应用')
    expect(translate('userCenter.permissions.actions.create')).toBe('新建权限')
    expect(translate('userCenter.permissions.actions.delete')).toBe('删除权限')
    expect(translate('userCenter.permissions.feedback.saved')).toBe('权限信息已保存。')
    expect(enumLabel('rbacPermissionTargetType', 'resource')).toBe('资源')
    expect(enumLabel('rbacPermissionTargetType', 'knowledge')).toBe('知识')
    expect(translate('userCenter.roles.menuGroups.workspace')).toBe('工作区')
    expect(translate('userCenter.roles.menuGroups.userCenter')).toBe('用户中心')
    expect(translate('userCenter.roles.menuGroups.project')).toBe('项目')

    i18n.global.locale.value = 'en-US'

    expect(translate('dashboard.header.eyebrow')).toBe('Workspace Dashboard')
    expect(translate('conversation.stream.title')).toBe('Message Stream')
    expect(translate('settings.tabs.general')).toBe('General Settings')
    expect(resolveCopy('sidebar.navigation.overview')).toBe('Overview')
    expect(resolveCopy('connections.header.title')).toBe('Workspace Connections')
    expect(enumLabel('conversationIntent', 'paused')).toBe('Paused')
    expect(enumLabel('recordStatus', 'active')).toBe('Active')
    expect(enumLabel('passwordState', 'set')).toBe('Set')
    expect(enumLabel('rbacPermissionKind', 'atomic')).toBe('Atomic')
    expect(enumLabel('rbacPermissionTargetType', 'user')).toBe('User')
    expect(enumLabel('menuSource', 'main-sidebar')).toBe('Main Sidebar')
    expect(translate('userCenter.profile.edit.title')).toBe('Edit Profile')
    expect(translate('userCenter.profile.access.title')).toBe('Access Overview')
    expect(translate('userCenter.profile.access.openRoles')).toBe('View roles')
    expect(translate('userCenter.profile.edit.fields.displayName')).toBe('Display name')
    expect(translate('userCenter.profile.edit.fields.avatar')).toBe('Avatar')
    expect(translate('userCenter.profile.edit.hints.avatarCurrent')).toBe('Current avatar')
    expect(translate('userCenter.profile.edit.actions.pickAvatar')).toBe('Choose avatar')
    expect(translate('userCenter.profile.password.title')).toBe('Change Password')
    expect(translate('userCenter.profile.password.fields.currentPassword')).toBe('Current password')
    expect(translate('userCenter.profile.feedback.profileSaved')).toBe('Profile saved.')
    expect(translate('userCenter.profile.feedback.passwordUpdated')).toBe('Password updated.')
    expect(translate('userCenter.profile.validation.passwordMismatch')).toBe('The new passwords do not match.')
    expect(translate('userCenter.users.actions.create')).toBe('New member')
    expect(translate('userCenter.users.actions.delete')).toBe('Delete member')
    expect(translate('userCenter.users.fields.role')).toBe('Role')
    expect(translate('userCenter.users.fields.scopeProjects')).toBe('Project scope')
    expect(translate('userCenter.users.avatar.actions.useDefault')).toBe('Use default avatar')
    expect(translate('userCenter.users.password.actions.useDefault')).toBe('Use default password')
    expect(translate('userCenter.roles.actions.create')).toBe('New role')
    expect(translate('userCenter.roles.actions.delete')).toBe('Delete role')
    expect(translate('userCenter.roles.fields.permissions')).toBe('Permissions')
    expect(translate('userCenter.roles.fields.menus')).toBe('Menus')
    expect(translate('userCenter.roles.menuGroups.app')).toBe('App')
    expect(translate('userCenter.permissions.actions.create')).toBe('New permission')
    expect(translate('userCenter.permissions.actions.delete')).toBe('Delete permission')
    expect(translate('userCenter.permissions.feedback.saved')).toBe('Permission saved.')
    expect(enumLabel('rbacPermissionTargetType', 'resource')).toBe('Resource')
    expect(enumLabel('rbacPermissionTargetType', 'knowledge')).toBe('Knowledge')
    expect(translate('userCenter.roles.menuGroups.workspace')).toBe('Workspace')
    expect(translate('userCenter.roles.menuGroups.userCenter')).toBe('User Center')
    expect(translate('userCenter.roles.menuGroups.project')).toBe('Project')
  })
})
